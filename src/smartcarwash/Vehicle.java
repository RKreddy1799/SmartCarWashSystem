package smartcarwash;

public class Vehicle {
	String name;
	String description;
	String washingDetergent;

	public String getWashingDetergent() {
		return washingDetergent;
	}

	public void setWashingDetergent(String washingDetergent) {
		this.washingDetergent = washingDetergent;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}

}
